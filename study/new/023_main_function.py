"""
025: main function (if __name__ == "__main__":)
파이썬 파일이 직접 실행되었는지, 아니면 모듈로써 import 되었는지 구분하는 마법의 코드입니다.
"""

# 만약 어떤 파이썬 파일 안에 아래와 같은 코드가 그냥 적혀있다면?
print("이 코드는 파일이 읽히자마자 무조건 실행됩니다.")

# 다른 파일에서 이 파일을 import 하기만 해도 위 print 문이 실행되어 버립니다! (의도치 않은 동작)
# 이를 방지하기 위해 파이썬 개발자들은 아래와 같은 관용구를 사용합니다.

def main():
    print("\n이 함수는 메인 로직을 담고 있습니다.")
    print("이 파일이 다른 곳에서 import 될 때는 절대 자동 실행되지 않습니다.")

# __name__ 은 파이썬이 내부적으로 사용하는 특별한 내장 변수(Magic Variable)입니다.
# 현재 파일을 터미널에서 직접 실행(python 025_main_function.py)하면 __name__에 "__main__" 이라는 문자열이 들어갑니다.
# 반대로 다른 파일에서 import로 불러오면 __name__에는 이 파일의 이름("025_main_function")이 들어갑니다.

if __name__ == "__main__":
    print(f"\n현재 __name__ 변수의 값은: {__name__}")
    print("스크립트가 직접 실행되었으므로 main() 함수를 호출합니다.")
    main()